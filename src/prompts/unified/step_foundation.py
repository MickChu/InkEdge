"""
统一基础设定模板 — inkOS 风格

一次 LLM 调用输出全部 5 个 SECTION：
  story_frame / volume_map / roles / book_rules / pending_hooks

世界观不独立成篇，而是 story_frame 的第 3 段——作为故事的有机组成，
不是百科全书。
"""
from src.prompts.base_template import TemplateStep

UNIFIED_FOUNDATION_STEP = TemplateStep(
    order=1,
    name="统一基础设定",
    description="一次生成完整基础设定（故事框架 + 世界观底色 + 卷地图 + 角色卡 + 规则 + 伏笔）",
    required_params=["topic", "genre", "number_of_chapters", "word_number", "user_guidance"],
    optional_params=["worldbuilding_depth"],
    output_key="unified_foundation",
    prompt="""你是这本书的总架构师。你输出的不是条目化的设定表，而是散文密度的基础骨架——后面写手和规划师能不能写出活人，从这里开始决定。

## 书籍信息
- 主题：{topic}
- 题材：{genre}
- 目标章数：{number_of_chapters}章 / ~{word_number}字/章
- 额外指导：{user_guidance}

## 核心原则
- 散文，不是表格。不要 bullet list，不要 JSON。写成能被人读下去的段落。
- 每个事实只在它的权威位置出现一次。主角弧线只写在 roles；世界铁律只写在 story_frame 段3；钩子只写在 pending_hooks。
- 世界观是一道调味料，不是百科全书——3-5 条不可以打破的规则 + 一个感官锚就够。
- 预算就是纪律，超了必须删。

## 输出结构（5 个 SECTION）

=== SECTION: story_frame ===

这是你最重要的输出。4 段散文，每段 400-800 字。控制整段在 3000 字以内。

### 段 1：主题与基调
这本书的核心命题是什么？不是"讲主角从弱到强"，而是具体的矛盾（"被时代按进泥里的人如何选择不被改写"、"当所有人说谎时坚持记录真相的代价"）。基调——温情冷冽悲壮肃杀，为什么是这个？用一句话指向主角（"本书主角是 X，弧线见 roles"）。

### 段 2：核心冲突
主要冲突不是"正邪对抗"，而是"因为 A 相信 X、B 相信 Y，他们必须在对撞"。对手至少两个——一个显性对手 + 一个结构性对手（体制/环境/命运），每个对手都有自己的内在逻辑。
**必须写清前台/后台双层故事**：
- 前台：读者每章看得到的表层冲突（查案/升级/搞事业），每卷有独立显性目标
- 后台：贯穿全书的暗线（幕后真相/身世秘密/体制压迫），碎片埋设，结局兑现
前后台必须有因果关联——每一段前台冲突背后都应该有后台的齿轮在转。

### 段 3：世界观底色
这个世界的运行规则——3-5 条不可违反的铁律，以散文写出，不要列表。
这个世界的质感——湿的还是干的？快的还是慢的？噪的还是静的？给写手一个明确的感官锚。
如果有年代/历史锚点（如"嘉靖三十七年"），自然织入文中；如果是架空世界，写清世界运行的基本逻辑。
一些好的世界观句子是这样的："这个世界的法力来自于记忆——施法越多，忘得越快。最强大的术士，往往连自己的名字都不记得。"
——而不是："力量体系：1. 法力来源：记忆 2. 施法代价：遗忘 3. 极限：姓名"

### 段 4：终局方向
最后一章的大致感觉——不是"主角登顶"这种空话，而是最后一个镜头长什么样。主角在哪、身边有谁、心里想什么。
末尾必须写出一句话全书 Objective：这本书完结时主角必须达成的一个可验证状态（例："从一个杂役成为宗门长老并公开父辈冤案的真相"）。它是后面卷规划的根。

=== SECTION: volume_map ===

分卷散文地图，5000 字以内。写清每卷主题、情绪流动（哪里压、哪里爽），卷间钩子与回收承诺，卷尾必须发生的不可逆事件。
末尾附 6 条节奏原则——至少 3 条具体到本书（"前 30 章每 5 章一个小爽点"），其余可用通用原则。

=== SECTION: roles ===

一人一卡 prose。用 ---ROLE--- 分隔。每个角色包含：核心标签、反差细节、小传、当前状态、与主角关系、内在驱动。
主角额外包含完整角色弧线（起点 → 终点 → 代价）。至少 3 个主要角色 + 2-3 个次要角色。总字数控在 8000 以内。

---ROLE---
tier: major
name: （角色名）
---CONTENT---
（散文角色卡）

=== SECTION: book_rules ===

仅输出 YAML frontmatter，零散文。所有叙事规则已经融合到 story_frame 的世界观底色中，这里只写硬限制：
```yaml
protagonist:
  name: （主角名）
  personalityLock: [3-5个关键词]
  behavioralConstraints: [3-5条行为约束]
genreLock:
  primary: {genre}
  forbidden: [2-3种禁止混入的文风]
prohibitions: [3-5条绝不出现的情节]
```

=== SECTION: pending_hooks ===

初始伏笔清单。每行一个钩子，格式：`[startChapter=0] 钩子描述 → 预计回收卷`。
至少 5 条，包括 3 条前台钩子 + 2 条后台钩子。

---

你的输出必须以 === SECTION: story_frame === 开头，严格按上述 5 个 SECTION 顺序，不要遗漏任何一个。""",
)
